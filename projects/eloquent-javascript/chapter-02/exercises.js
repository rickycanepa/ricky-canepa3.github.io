// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function drawChessboard(num) {
let board = "";
for(let i = 0; i < num; i++){
  for(let y = 0; y < num; y++){
    if((i + y) % 2 === 0){
      board = board + " ";
    }else{
      board = board + "#";
    }
  }
  board = board + "\n";
}
console.log(board);
}

